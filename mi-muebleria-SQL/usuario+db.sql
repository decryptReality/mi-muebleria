USE Muebleria;

CREATE TABLE TiposPiezas
(
	tipoPieza VARCHAR(64),
	PRIMARY KEY(tipoPieza)
);

CREATE TABLE TiposMuebles
(
	tipoMueble VARCHAR(64),
	PRIMARY KEY(tipoMueble)
);

CREATE TABLE Usuarios
(
	nombreUsuario VARCHAR(32),
	contrasenia VARCHAR(32) NOT NULL,
	areaTrabajo ENUM('1','2','3') NOT NULL,
	/* [1,fabrica],[2,ventas],[3,administracion] */
	PRIMARY KEY(nombreUsuario)
);

CREATE TABLE Clientes
(
	nit VARCHAR(16),
	nombreCliente VARCHAR(64) NOT NULL,
	direccion VARCHAR(64),
	municipio VARCHAR(32),
	departamento VARCHAR(32),
	PRIMARY KEY(nit)
);

CREATE TABLE Muebles
(
	idMueble INT AUTO_INCREMENT,
	tipoMueble VARCHAR(64) NOT NULL,
	precioEnsamble FLOAT(9,2) DEFAULT 0 NOT NULL,
	fechaEnsamble DATE NOT NULL,
	nombreUsuario VARCHAR(32) NOT NULL,
	estadoMueble ENUM('0','1','2') DEFAULT '0',
	/* [0,enPiezas],[1,ensamblado],[2,vendido] */
	PRIMARY KEY(idMueble),
	FOREIGN KEY(tipoMueble) REFERENCES TiposMuebles(tipoMueble),
	FOREIGN KEY(nombreUsuario) REFERENCES Usuarios(nombreUsuario)
);

CREATE TABLE Facturas
(
	idFactura VARCHAR(16),
	nit VARCHAR(16) NOT NULL,
	fechaVenta DATE NOT NULL,
	nombreUsuario VARCHAR(32) NOT NULL,
	PRIMARY KEY(idFactura),
	FOREIGN KEY(nit) REFERENCES Clientes(nit),
	FOREIGN KEY(nombreUsuario) REFERENCES Usuarios(nombreUsuario)
);

CREATE TABLE Piezas
(
	tipoPieza VARCHAR(64) NOT NULL,
	precioUnidad FLOAT(9,2) NOT NULL,
	unidadesDisponibles INT DEFAULT 0 NOT NULL,
	FOREIGN KEY(tipoPieza) REFERENCES TiposPiezas(tipoPieza),
	UNIQUE KEY(tipoPieza,precioUnidad)
);

CREATE TABLE Libro 
(
	tipoMueble VARCHAR(64) NOT NULL,
	tipoPieza VARCHAR(64) NOT NULL,
	unidadesRequeridas SMALLINT NOT NULL,
	FOREIGN KEY(tipoMueble) REFERENCES TiposMuebles(tipoMueble),
	FOREIGN KEY(tipoPieza) REFERENCES TiposPiezas(tipoPieza),
	UNIQUE KEY(tipoMueble,tipoPieza)
);

CREATE TABLE Ventas
(
	idMueble INT NOT NULL,
	precioVenta FLOAT(9,2) NOT NULL,
	ganancia FLOAT(9,2) DEFAULT 0 NOT NULL ,
	idFactura VARCHAR(16) NOT NULL,
	FOREIGN KEY(idMueble) REFERENCES Muebles(idMueble),
	FOREIGN KEY(idFactura) REFERENCES Facturas(idFactura),
	UNIQUE KEY(idMueble)
);

CREATE TABLE Devoluciones
(
	idMueble INT NOT NULL,
	fechaDevolucion DATE NOT NULL,
	perdida FLOAT(9,2) NOT NULL,
	nit VARCHAR(16) NOT NULL,
	FOREIGN KEY(idMueble) REFERENCES Muebles(idMueble),
	FOREIGN KEY(nit) REFERENCES Clientes(nit),
	UNIQUE KEY(idMueble)
);

